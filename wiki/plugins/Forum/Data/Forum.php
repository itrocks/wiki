<?php
namespace SAF\Wiki;

class Forum
{
	//---------------------------------------------------------------------------------------- $title
	/**
	 * @var string
	 */
	var $title;

	//------------------------------------------------------------------------------------- $category
	/**
	 * @link Object
	 * @var Category
	 */
	var $category;

	//-------------------------------------------------------------------------------------- $content
	/**
	 * Description
	 * @var string
	 */
	var $content;

	//----------------------------------------------------------------------------------- $moderators
	/**
	 * @link Object
	 * @var \SAF\Framework\User[]
	 */
	var $moderators;

	//------------------------------------------------------------------------------------ $nb_topics
	/**
	 * @var int
	 */
	var $nb_topics;

	//------------------------------------------------------------------------------------- $nb_posts
	/**
	 * @var int
	 */
	var $nb_posts;

	//------------------------------------------------------------------------------------ $last_post
	/**
	 * @link Object
	 * @var Post
	 */
	var $last_post;

	//------------------------------------------------------------------------------------- $position
	/**
	 * The position
	 * @var int
	 */
	var $position;

	public function __toString()
	{
		return $this->title . "";
	}

}
