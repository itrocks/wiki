<?php
namespace SAF\Wiki;

/**
 * @representative title
 */
class Topic
{
	//---------------------------------------------------------------------------------------- $title
	/**
	 * @var string
	 */
	var $title;

	//---------------------------------------------------------------------------------------- $forum
	/**
	 * @link Object
	 * @var Forum
	 */
	var $forum;

	//----------------------------------------------------------------------------------- $first_post
	/**
	 * @link Object
	 * @var Post
	 */
	var $first_post;

	//------------------------------------------------------------------------------------ $last_post
	/**
	 * @link Object
	 * @var Post
	 */
	var $last_post;

	//-------------------------------------------------------------------------------------- $nb_view
	/**
	 * @var int
	 */
	var $nb_view;

	//-------------------------------------------------------------------------------------- $nb_post
	/**
	 * @var int
	 */
	var $nb_post;

	//--------------------------------------------------------------------------------------- $closed
	/**
	 * @var boolean
	 */
	var $closed;

	public function __toString()
	{
		return $this->title . "";
	}

}
